extension UIImage {
    /**
     *  重设图片大小
     */
    func reSizeImage(reSize:CGSize)->UIImage {
        //UIGraphicsBeginImageContext(reSize);
        UIGraphicsBeginImageContextWithOptions(reSize,false,UIScreen.main.scale);
        self.draw(in: CGRect(x: 0, y: 0, width: reSize.width, height: reSize.height))
        let reSizeImage:UIImage = UIGraphicsGetImageFromCurrentImageContext()!;
        UIGraphicsEndImageContext();
        return reSizeImage;
    }
    /**
     *  根据宽度进行等比例的缩放
     */
    func ratioResizeForWidth(dWidth:CGFloat)->UIImage {
        //UIGraphicsBeginImageContext(reSize);
        let width:CGFloat = self.size.width
        let height:CGFloat = self.size.height
        let dHeight:CGFloat = height/width*dWidth
        let newSize:CGSize = CGSize(width: dWidth, height: dHeight)
        UIGraphicsBeginImageContextWithOptions(newSize,false,UIScreen.main.scale);
        self.draw(in: CGRect(x: 0, y: 0, width: dWidth, height: dHeight))
        let reSizeImage:UIImage = UIGraphicsGetImageFromCurrentImageContext()!;
        UIGraphicsEndImageContext();
        return reSizeImage;
    }
    
    /**
     *  等比率缩放
     */
    func scaleImage(scaleSize:CGFloat)->UIImage {
        let reSize = CGSize(width: self.size.width * scaleSize, height: self.size.height)
        // let reSize = CGSize(self.size.width * scaleSize, self.size.height * scaleSize)
        return reSizeImage(reSize: reSize)
    }
